/**
 * 构建后脚本：生成 Service Worker 预缓存清单
 * 扫描 dist/assets 目录，生成所有需要预缓存的资源列表
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const distDir = path.join(__dirname, '../dist');
const assetsDir = path.join(distDir, 'assets');

// 需要预缓存的文件扩展名
const CACHE_EXTENSIONS = ['.js', '.css', '.woff', '.woff2'];

function getAllFiles(dir, files = []) {
  if (!fs.existsSync(dir)) return files;
  
  const items = fs.readdirSync(dir);
  for (const item of items) {
    const fullPath = path.join(dir, item);
    const stat = fs.statSync(fullPath);
    if (stat.isDirectory()) {
      getAllFiles(fullPath, files);
    } else {
      files.push(fullPath);
    }
  }
  return files;
}

function generateManifest() {
  const files = getAllFiles(assetsDir);
  const manifest = [];
  
  for (const file of files) {
    const ext = path.extname(file).toLowerCase();
    if (CACHE_EXTENSIONS.includes(ext)) {
      // 转换为相对于 dist 的 URL 路径
      const relativePath = '/' + path.relative(distDir, file).replace(/\\/g, '/');
      manifest.push(relativePath);
    }
  }
  
  // 添加根路径资源
  manifest.push('/');
  manifest.push('/index.html');
  manifest.push('/manifest.json');
  
  // 生成清单文件
  const manifestContent = `// Auto-generated by generate-sw-manifest.js
// Generated at: ${new Date().toISOString()}
const PRECACHE_ASSETS = ${JSON.stringify(manifest, null, 2)};
`;
  
  // 读取 sw.js 模板并注入清单
  const swTemplatePath = path.join(__dirname, '../logo/sw.js');
  let swContent = fs.readFileSync(swTemplatePath, 'utf-8');
  
  // 替换 STATIC_ASSETS 为完整的预缓存列表
  swContent = swContent.replace(
    /const STATIC_ASSETS = \[[\s\S]*?\];/,
    `const STATIC_ASSETS = ${JSON.stringify(manifest, null, 2)};`
  );
  
  // 更新缓存版本号（使用时间戳）
  const version = Date.now().toString(36);
  swContent = swContent.replace(
    /const CACHE_NAME = '[^']+';/,
    `const CACHE_NAME = 'owlplayer-v${version}';`
  );
  swContent = swContent.replace(
    /const STATIC_CACHE_NAME = '[^']+';/,
    `const STATIC_CACHE_NAME = 'owlplayer-static-v${version}';`
  );
  swContent = swContent.replace(
    /const DYNAMIC_CACHE_NAME = '[^']+';/,
    `const DYNAMIC_CACHE_NAME = 'owlplayer-dynamic-v${version}';`
  );
  
  // 写入到 dist 目录
  const swOutputPath = path.join(distDir, 'sw.js');
  fs.writeFileSync(swOutputPath, swContent);
  
  console.log(`[SW Manifest] Generated with ${manifest.length} assets`);
  console.log(`[SW Manifest] Cache version: v${version}`);
}

generateManifest();
